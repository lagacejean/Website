  <h2>À propos</h2>

  <h3>Public cible</h3>

  <img class="left" src="images/cumc2011.jpg" width=320 height=180 alt="CCÉM 2011">
  <p>
    Le Congrès Canadien des Étudiants en Mathématiques est un
    évènement organisé par les étudiants pour les étudiants. Plus
    particulièrement, il est conçu pour les étudiants de premier cycle
    universitaire. Néanmoins, les étudiants de tous les niveaux sont
    les bienvenus au congrès et tout un chacun est invité à présenter
    une conférence pour autant que l'exposé s'adresse à des étudiants
    de premier cycle.
  </p>

  <p>
    L'évènement est national; les étudiants de partout au pays sont
    donc encouragés à être présents. C'est d'ailleurs la diversité
    associée à la présence d'étudiants de chaque grande région qui
    rend l'évènement si pertinent. Et, bien que l'organisation soit
    pensée à l'échelle nationale, la notoriété du congrès attire
    maintenant des étudiants internationaux! Quiconque intéressé par
    l'évènement est invité à prendre part à ce grand rassemblement.
  </p>

  <h3>Les grandes valeurs</h3>

  <p>
    La présence d'étudiants de plusieurs régions du Canada reflète
    bien une des grandes valeurs sous-jacentes à l'évènement. En
    effet, le coeur même du CCÉM est de promouvoir la diversité. Cela
    signifie de rassembler des étudiants de partout au pays, mais
    également d'encourager la diversité d'opinion et le bilinguisme.
  </p>

  <p>
    Pour ce faire, le CCÉM se distingue par l'absence totale de
    compétition durant tout le déroulement de l'évènement. L'objectif
    est de fournir un contexte collaboratif où chacun est invité à
    prendre part aux activités. L'approche est ainsi centrée sur les
    rencontres qui, nous l'espérons, mèneront éventuellement à des
    collaborations fructueuses.
  </p>

  <p>
    Cette année, le comité organisateur a voulu ajouter un principe
    supplémentaire sur lequel baser le CCÉM. L'objectif est
    d'organiser un évènement écoresponsable, c'est-à-dire de réduire
    le plus possible l'empreinte écologique associée à tout ce qui a
    trait à l'organisation. Les scientifiques s'accordent désormais
    sur la présence d'un réchauffement climatique à l'échelle
    planétaire et il devient de plus en plus important de prendre des
    mesures à ce sujet. Le prochain congrès tentera ainsi de réduire
    son impact sur l'équilibre climatique.
  </p>

  <h3>Fonctionnement</h3>

  <img class="right" src="images/cumc2012.jpg" width=300 height=200 alt="CCÉM 2012">
  <p>
    Le CCÉM consiste d'abord en une série d'exposés sur les
    mathématiques que les participants se présentent entre
    eux. L'horaire est construit de façon à présenter plusieurs
    exposés de durée égale au même moment. Ainsi, tous pourront
    trouver un sujet qui les intéresse. Sur une base volontaire, les
    participants sont donc invités à présenter un sujet de leur choix
    devant leur pairs. Il s'agit d'une expérience enrichissante tant
    pour le conférencier qui pratique ses talents de communicateur que
    pour le spectateur qui s'ouvre à une panoplie de sujets.
  </p>

  <p>
    Entre les exposés étudiants, les participants seront invités à
    assister à des conférences plénières. Ces dernières, plus longues,
    seront données par des conférenciers chevronnés invités
    directement par le comité organisateur. Ces conférences sont
    l'occasion de réunir tous les participants et permettent d'assurer
    une visibilité à chacune des grandes branches des mathématiques.
  </p>

  <p>
    Si vous songez à participer au congrès, nous vous invitons à
    considérer présenter un exposé. Si l'expérience vous tente, vous
    trouverez plus d'information dans la section
    des <a href="index.php?content=faq">questions fréquentes</a>.
  </p>
</div>
