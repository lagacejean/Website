  <h3>Questions des commanditaires</h3>

  <div class="question-first" id="when">
    <p>
	Quand l'évèment se déroulera-t-il?
    </p>
  </div>

  <div class="answer-first">
    <p>
      R: Le CCÉM se déroule du 10 au 14 juillet 2013, à l'Université
      de Montréal. Un banquet de fermeture se tiendra le samedi 13
      juillet 2013.
    </p>
  </div>

  <div class="question" id="who">
    <p>
      Q: Qui organise cet évènement?
    </p>
  </div>

  <div class="answer">
    <p>
      R: Le CCÉM est entièrement organisé par
      un <a href="index.php?content=committee">comité d'étudiants</a>
      en mathématiques et en physique mathématique. C'est ce comité
      qui est en charge du projet et qui prend toutes les décisions
      relatives à l'évènement. Le mandat d'organisation est supervisé
      par le <a href="http://studc.cms.math.ca/indexfr.html">STUDC</a>
      qui assure la perrenité de l'évènement années après années entre
      les différents comités. Notons que les membres du comité ne sont
      pas rémunérés pour le travail qu'ils accomplissent.
    </p>
  </div>

  <div class="question-last" id="why-funding">
    <p>
      Q: Pourquoi devrait-on fournir du financement?
    </p>
  </div>

  <div class="answer-last">
    <p>
      R: Le CCÉM rassemble environ 200 des meilleurs étudiants de
      premier cycle en mathématiques au pays. Il s'agit du plus grand
      évènement du genre en amérique, puisqu'il s'agit d'un évènement
      d'envergure nationale. Augmenter la visibilité de votre centre
      de recherche, de votre département ou de votre entreprise auprès
      d'eux permet d'attirer les futures candidats passionnées et
      talentueux qui forment la relève mathématique. Que ce soit pour
      des études graduées, des offre de stages, des emplois à combler
      ou un marché à percer, l'évènement est une immense plateforme
      pour faire connaître votre institution. Associer le nom de votre
      institution à cette évènement, c'est présenter votre instution
      auprès des étudiants de premier cycle les plus impliqués en
      mathématiques à travers le pays au complet.
    </p>
