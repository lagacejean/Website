  <div class="question-first" id="funding-myself">
    <p>
      Q: Comment puis-je obtenir du financement pour mes dépenses dues
      au CCÉM?
    </p>
  </div>

  <div class="answer-first">
    <p>
      R: Commence par contacter ton association étudiante.  Elle a
      peut-être fait des arrangements pour les étudiants qui veulent
      participer au congrès. Aussi, si tu as un stage pendant l'été,
      parles-en à ton superviseur. Il aura peut-être des fonds
      disponibles pour ton voyage, ou peut-être même pourra-t-il te
      financer à même son fonds de recherche!
    </p>

    <p>
      Contacte aussi ton département, ta faculté, ou n'importe quelle
      autre organisation avec laquelle tu es affilié. Parle-leur du
      CCÉM et montre-leur notre site web et notre affiche. Dis-leur
      dans quelle situation tu te trouves. Pour les convaincre,
      n'oublie pas que tu représenteras ton université au CCÉM. C'est
      de cette façon que la plupart des participants peuvent venir
      chaque année. En effet la plupart des départements et des
      facultés disposent de fonds pour des situations comme
      celle-ci. Pour rendre ton argument plus convaincant et faire
      preuve de ton implication, prépare une conférence. Tu auras
      ainsi plus de facilité à démontrer comment tu représenteras ton
      université concrètement.
    </p>
  </div>

  <div class="question" id="better-participant">
    <p>
      Q: Est-ce que les autres participants seront meilleurs que moi
      en mathématiques?
    </p>
  </div>

  <div class="answer">
    <p>
      R: Le CCÉM s'adresse à tous les étudiants de premier cycle qui
      sont intéressés par les mathématiques et les domaines reliés. Le
      congrès n'est pas une compétition, mais plutôt un rassemblement
      d'amoureux des mathématiques de partout au Canada et parfois
      même d'autres pays! On recommande fortement aux étudiants de
      tous les niveaux de participer et on rappelle qu'il n'y aura
      aucun élément de compétition pendant le congrès, à l'exception
      bien sûr des candidatures pour l'organisation du CCÉM 2014!
    </p>
  </div>

  <div class="question" id="half-present">
    <p>
      Q: Je ne peux pas être présent pour l'ensemble du
      congrès. Devrais-je quand même venir pour une partie de
      celui-ci?
    </p>
  </div>

  <div class="answer">
    <p>
      R: Le tarif d'inscription est fixe quel que soit le nombre de
      jours auxquels tu comptes participer. Malgré tout, l'hébergement
      sera moins dispendieux. Si tu peux être là pendant la majorité
      du congrès, nous t'encourageons à venir, car la liste de
      conférenciers sera excellente. De plus, n'oublie pas aussi tout
      ce qui n'est pas sur le programme! Tu rencontreras des gens de
      partout au Canada qui s'intéressent aux mathématiques.  Cela
      constitue tout de même un bon investissement puisqu'il s'agit
      d'une expérience incroyable!
    </p>
  </div>

