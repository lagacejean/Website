    <p>
      Q: Pourquoi devrait-on fournir du financement à cet évènement?
    </p>
  </div>

  <div class="answer-last">
    <p>
      R: Le CCÉM rassemble environ 200 des meilleurs étudiants de
      premier cycle en mathématiques du pays. Augmenter la visibilité
      de votre centre de recherche, de votre département ou de votre
      entreprise auprès d'eux vous permet d'attirer des candidats
      passionnés et talentueux, que ce soit pour des études graduées
      ou pour le marché du travail.
