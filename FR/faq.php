  <div class="question" id="which-subject">
    <pt>
      Q: Je souhaite présenter une conférence. De quoi devrais-je
      parler?
    </p>
  </div>

  <div class="answer">
    <p>
      R: Parle de quelque chose qui t'intéresse. Si tu trouves le
      sujet fascinant, il y a de fortes chances qye ton public
      l'apprécie aussi. Évidemment, il vaut mieux que ce soit relié
      aux mathématiques, mais cela peut être entendu au sens
      large. N'oublie pas qu'une des valeurs de l'évènement est de
      promouvoir la diversité. Des choix de sujets possibles, sans
      imposer de limites, pourraient être autour de ce que tu as vu
      dans un des tes cours et que tu as apprécié, quelque chose que
      tu as lu récemment, un sujet sur lequel tu t'es déjà cassé la
      tête, un sujet de recherche... Toutes les idées sont bonnes!
    </p>

    <p>
      Si tu penses que ta conférence ne sera accessible qu'à des
      étudiants avec un fort bagage dans un domaine précis, il est
      possible de l'indiquer au moment de la soumission de ton résumé.
    </p>
  </div>

  <div class="question" id="not-expert">
    <p>
      Q: Je trouve un sujet intéressant, mais je ne suis pas un expert
      en la matière. Devrais-je quand même en parler?
    </p>
  </div>

  <div class="answer">
    <p>
      R: Certainement! La préparation de ta conférence sera pour toi
      une opportunité d'en apprendre plus sur le sujet. Assure-toi
      toutefois de te préparer assez longtemps d'avance pour pouvoir
      acquérir une certaine aisance à propos de ton sujet avant ta
      présentation! De toute façon, ton public ne sera probablement
      pas expert dans le sujet non plus. Cependant, pour ne pas avoir
      de regret, il vaut mieux éviter de viser trop haut .  Fie-toi à
      tes capacités.  </p>
  </div>

  <div class="question" id="how-long">
    <p>
      Q: Quelle est la durée d'une conférence? Combien de temps
      devrait durer ma conférence?
    </p>
  </div>

  <div class="answer">
    <p>
      R: Il y a deux options: 20 minutes et 45 minutes. Si tu ne sais
      pas quelle durée choisir, voici une suggestion: fais une
      conférence de 20 minutes. Pourquoi? Parce que c'est un format
      idéal! En effet, parler pendant 45 minutes demande un certain
      effort, surtout si c'est ta première fois. Puisqu'une conférence
      de 20 minutes demande moins de contenu, cela aide à maintenir le
      contact avec le public et permet de passer plus facilement aux
      points essentiels. Les autres participants apprécieront une
      telle conférence. Si tu en as trop sur le coeur, dis-toi
      qu'après la conférence, les gens les plus intéressés resteront
      pour en discuter avec toi.
    </p>
  </div>

  <div class="question" id="time-myself">
    <p>
      Q: Comment est-ce que je peux déterminer la durée de ma
      conférence?
    </p>
  </div>

  <div class="answer">
    <p>
      R: Essaie d'évaluer la quantité de matériel que tu voudrais
      présenter. Pratique-toi et, surtout, chronomètre-toi et pas
      qu'une fois! Tu sauras ainsi combien de temps il te
      faut. N'oublie pas que c'est plus difficile de garder
      l'attention d'un groupe pendant 45 minutes que pendant 20
      minutes. Cependant, si ton sujet est intéressant et que tu es
      bien préparé, 45 minutes peuvent passer très vite.  C'est à toi
      de voir.  </p>
  </div>

  <div class="question" id="which-language">
    <p>
      Q: Devrais-je donner ma conférence en français ou en anglais?
    </p>
  </div>

  <div class="answer">
    <p>
      R: Tu devrais donner ta conférence dans la langue dans laquelle
      tu te sens le plus confortable. Tu peux aussi essayer de la
      donner dans une langue et de préparer les diapositives dans
      l'autre. Enfin, il est possible de mentionner à l'auditoire que
      tu prends les questions en français et en anglais.
    </p>
  </div>

  <div class="question" id="get-confident">
    <p>
      Q: Je suis très nerveux, car c'est la première fois que je donne
      une conférence. Que me conseillez-vous?
    </p>
  </div>

  <div class="answer">
    <p>
      R: Détends-toi et prends une grande respiration. Il y a
      plusieurs bonnes réponses à cette question. Garde surtout en
      tête que même si la conférence n'est pas parfaite, ce n'est pas
      grave. Il n'y a pas de bonnes ou de mauvaises conférences,
      seulement des conférences qui nous intéressent ou non. En
      ajoutant une touche d'humour à la présentation, tu t'assureras
      que ton auditoire te suivra tout au long de la conférence et tu
      rendras le tout plus intéressant.
    </p>

    <p>
      Néanmoins, il y a quelques erreurs faciles à éviter. En général,
      une bonne préparation est le meilleur moyen de s'assurer une
      certaine confiance. Cela permet d'éviter la plupart des erreurs
      dues à la nervosité. Par exemple, il est inutile d'aller plus
      vite si tu manques de temps. Il vaut mieux couper directement
      des points de ta présentation. Les auditeurs ne s'en apercevront
      peut-être même pas!
    </p>
  </div>

  <div class="question" id="same-time">
    <p>
      Q: Il y a deux conférences auxquelles j'aimerais assister qui se
      donnent en même temps. Que puis-je faire?
    </p>
  </div>

  <div class="answer">
    <p>
      R: Si cela arrive, plusieurs options s'offrent à
      toi. Premièrement, tu peux demander à un ami d'aller à l'autre
      conférence et de prendre des notes pour toi. Sinon, va voir le
      conférencier en question et demande-lui de te fournir des notes
      ou des références qui te permettront d'en apprendre plus sur le
      sujet plus tard.
    </p>
  </div>

  <div class="question" id="requirements">
    <p>
      Q: Il y a une conférence qui me semble très intéressante, mais
	dont je n'ai pas les préalables nécessaires. Puis-je y aller
	malgré tout?
    </p>
  </div>

  <div class="answer">
    <p>
      R: Souvent, on peut apprécier une conférence sans comprendre
      parfaitement tous les détails. Si tu es intéressé par le sujet,
      alors vas-y. Dans le pire des cas, tu obtiendras une motivation
      à acquérir le bagage nécessaire pour tout comprendre et tu auras
      appris quelque chose de nouveau. N'hésite pas non plus à aller
      discuter avec le conférencier après la conférence à propos des
      points que tu n'as pas saisis.
    </p>
  </div>

