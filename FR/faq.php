  <div class="question-first" id="funding-myself">
    <p>
      Q: Comment obtenir du financement pour mes dépenses dues
      au CCÉM?
    </p>
  </div>

  <div class="answer-first">
    <p>
      R: Commence par contacter ton association étudiante.  Elle a
      peut-être fait des arrangements pour les étudiants qui veulent
      participer au congrès. Aussi, si tu as un stage pendant l'été,
      parles-en à ton superviseur. Il aura peut-être du financement à
      t'offrir pour ton voyage.
    </p>

    <p>
      Essaie aussi ton département, ta faculté, ou n'importe quel
      département avec lequel tu es affilié. Parle-leur du CCÉM,
      montre-leur notre site web et notre affiche. Dis-leur dans
      quelle situation tu te trouves et à quel point ce congrès est
      profitable pour toi. Pour les convaincre, n'oublie pas que tu
      représenteras ton université parmi une communauté de
      mathématiciens de partout au Canada. En fait, c'est généralement
      grâce à ce genre de financement que la plupart des participants
      peuvent venir chaque année. En effet, la plupart des
      départements et des facultés disposent de fond pour les congrès
      et tout évènements scientifiques. Pour rendre ton argument plus
      convaincant et démontrer ton implication, il est souvent bien vu
      de préparer une conférence. Tu auras ainsi plus de facilité à
      démontrer à ton université comment tu la représenteras
      concrètement.
    </p>
  </div>

  <div class="question" id="better-participant">
    <p>
      Q: Est-ce que les autres participants seront meilleurs que moi
      en mathématiques?
    </p>
  </div>

  <div class="answer">
    <p>
      R: Le CCÉM s'adresse à tous les étudiants de premier cycle qui
      sont intéressés par les mathématiques et les domaines
      connexes. Le congrès n'est pas une compétition, mais plutôt un
      rassemblement d'amoureux des mathématiques de partout au Canada,
      et même parfois d'autre pays! On recommande fortement aux
      étudiants de tous les niveaux de se présenter, et on rappelle
      qu'il n'y a aura aucun élément de compétition pendant le
      congrès, à l'exception bien sûr des candidatures pour organiser
      le CCÉM 2014! Alors la question n'est pas de savoir si d'autres
      ont plus ou moins de facilité en mathématiques que toi, mais
      plutôt de savoir si tu as envie de les rencontrer.
    </p>
  </div>

  <div class="question" id="half-present">
    <p>
      Q: Je ne peux pas être présent pour l'ensemble du
      congrès. Devrais-je quand même venir pour une partie de
      celui-ci?
    </p>
  </div>


  <div class="answer">
    <p>
      R: Le tarif d'inscription est fixe, quelque soit le nombre de
      jour auxquels tu comptes participer. Malgré tout, l'hébergement
      sera moins dispendieux si tu planifies ne pas participer à la
      totalité du congrès. Si tu peux être présent pendant la plupart des
      jours du congrès, nous t'encourageons à venir, car la liste des
      conférenciers sera excellente. N'oublie pas aussi tout ce qui
      n'est pas sur le programme! Tu rencontreras plein de gens de
      partout au Canada qui s'intéressent aux mathématiques. Sous cet
      angle, il s'agit quand même un bon investissement, car il s'agit
      qu'une expérience incroyable.
    </p>
  </div>

