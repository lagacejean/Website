  <div class="question" id="which-subject">
    <p>
      Q: Je souhaite donner une conférence. De quoi devrais-je parler?
    </p>
  </div>

  <div class="answer">
    <p>
      R: Parle de quelque chose qui t'intéresse. Si tu trouves ton sujet
      fascinant, les chances sont que ton public aussi. Évidemment, il
      vaut mieux qu'il y ait un lien avec les mathématiques, mais cela
      peut être entendu au sens large. N'oublie pas qu'une des valeurs
      de l'évènement est de promouvoir la diversité. Des choix de
      sujets possibles, sans imposer de limites, pourraient être
      autour de ce que tu as vu dans un des tes cours et que tu as
      apprécié, quelque chose que tu as lu récemment, un sujet sur
      lequel tu t'es déjà cassé la tête, un sujet de recherche, un
      problème qui t'empêche de t'endormir le soir... Toutes les idées
      peuvent intéresser si elles t'intéressent toi-même.
    </p>

    <p>
      Si tu penses que ta conférence ne sera accessible qu'à seulement
      des étudiants avec un fort bagage dans un domaine précis, il est
      possible de l'indiquer au moment de la soumission de ton résumé.
      De cette façon, tu peux éviter les mauvaises surprises à ton
      auditoire. Par contre, il vaut mieux ne pas assumer que tout ton
      auditoire aura les préalables requis. Après tout, l'évènement
      propose surtout un contexte de découverte.
    </p>
  </div>

  <div class="question" id="not-expert">
    <p>
      Q: Je trouve un sujet intéressant, mais je ne suis pas un expert
      dans le domaine. Devrais-je quand même en parler?
    </p>
  </div>

  <div class="answer">
    <p>
      R: Certainement! La préparation de ta conférence sera pour toi
      une opportunité d'en apprendre plus sur le sujet. Assure-toi,
      toutefois, de te préparer assez longtemps d'avance pour avoir
      une certaine aisance. De cette façon, tu évites d'être trop
      nerveux au moment de présenter! N'oublie pas non plus que si tu
      n'es pas un expert dans ton sujet, ton public n'en connaîtra
      sûrement pas plus. Ils apprécieront alors d'entendre comment tu
      as compris certaines choses et pourquoi tu les apprécie.
    </p>
  </div>

  <div class="question" id="how-long">
    <p>
      Q: Quelle est la durée d'une conférence? Combien de temps
      devrais durer ma conférence?
    </p>
  </div>

  <div class="answer">
    <p>
      R: Il y a deux options: 20 minutes et 45 minutes. Si tu ne sais
      pas quelle durée choisir, voici ma suggestion: fais une
      conférence de 20 minutes. Pourquoi? Parce que c'est un format
      idéal! En effet, parler pendant 45 minutes demande un certain
      effort, surtout si c'est ta première fois. Puisqu'une conférence
      plus courte ne peut pas présenter autant de contenu, cela te
      forcera à choisir seulement les points essentielles du sujet que
      tu veux traiter. Les autres participants apprécieront une courte
      conférence qui saura aller aux points essentielles. Ainsi, tu
      auras une cerainte facilité à maintenir le contact avec le
      public. Si tu as l'impression qu'avec seulement 20 minutes, tu
      en auras encore trop sur le coeur, rappel toi qu'après la
      conférence, les gens les plus intéressés viendront en discuter
      avec toi.
    </p>

    <p>
      Une fois l'horaire établie, tu auras une période allouée qui
      correspondra au temps que tu as demandé. Pour les besoins de
      l'organisation, nous te demanderons de respecter ce temps pour
      que chacun puisse faire sa présentation. N'hésite surtout pas à
      te pratiquer avant le grand moment pour t'assurer que ta
      conférence entre bien dans les temps prévus.
    </p>
  </div>

  <div class="question" id="time-myself">
    <p>
      Q: Comment est-ce que je peux déterminer la durée de ma
      conférence?
    </p>
  </div>

  <div class="answer">
    <p>
      R: Essaie d'évaluer la quantité de matériel que tu voudrais
      présenter. Certains conférenciers proposent des règles strictes
      entre le nombre de diapositives et la durée d'une conférence. Il
      n'y a pas de consensus dans ce domaine, mais tu peux trouver
      beaucoup d'information à ce sujet sur internet. Mon seul
      conseil: pratique-toi et, surtout, chonomètre-toi. Et ne le fais
      pas qu'une fois! Tu sauras ainsi combien de temps il te faut, où
      tu perds le plus de temps, qu'est-ce que tu peux couper
      facilement, quelle partie te mélange le plus... N'oublie pas que
      c'est plus difficile de garder l'attention d'un groupe pour 45
      minutes que pour 20 minutes, mais que si ton sujet est
      intéressant et que tu es bien préparé,ta conférence peut passer
      très vite.
    </p>
  </div>

  <div class="question" id="which-language">
    <p>
      Q: Dans quelle langue devrais-je donner ma conférence? Français
      ou anglais?
    </p>
  </div>

  <div class="answer">
    <p>
      R: Tu devrais donner ta conférence dans la langue dans laquelle
      tu te sens le plus confortable. Tu peux aussi essayer de la
      donner dans une langue et de préparer les diapositives dans
      l'autre. Il est aussi possible d'accepter les questions dans les
      deux langues. Dans tous les cas, je t'invite à préciser ces
      possibilités dans ton résumé. De cette façon, tu encourages les
      gens qui ne parlent pas couramment ta langue à venir tout de
      même à ta conférence.
    </p>
  </div>

  <div class="question" id="get-confident">
    <p>
      Q: Je suis très nerveux, c'est la première fois que je donne une
      conférence, que me conseilles-tu?
    </p>
  </div>

  <div class="answer">
    <p>
      R: Détend toi et prends une grande respiration. Il y a plusieurs
      bonnes réponses à cette question. Garde surtout en tête que même
      si la conférence n'est pas parfaite, ce n'est pas grave; il n'y
      a pas de bonnes ou de mauvaises conférences, seulement des
      conférences qui nous intéressent ou non. En ajoutant une touche
      d'humour à la présentation, tu t'assureras que ton auditoire te
      suivra tout au long de la conférence en gardant le sourire.
    </p>

    <p>
      Néanmoins, il y a quelques erreurs faciles à éviter. En
      générale, une bonne préparation est le meilleur moyen de
      s'assurer une aisance sur scène. Cela permet d'éviter la plupart
      des erreurs qui sont dues à la nervosité. Tu ne peux pas être
      trop préparé. N'hésite pas à demander à quelqu'un d'assister à
      une avant-première pour te faire des commentaires. Une fois à
      Montréal, prends le temps de te familiariser avec la salle où tu
      présenteras. Aussi, tous les organisateurs seront heureux de
      répondre à tes questions une fois sur place. D'ailleurs, une
      présentation sur <em>Comment faire une bonne conférence</em> est
      prévu à l'ouverture du congrès. Tu auras donc quelques conseils
      de dernière minute. Si tu as d'autres
      questions, <a href="mailto:communication@ccem.math.ca">contacte
      nous</a>!
    </p>
  </div>

  <div class="question" id="same-time">
    <p>
      Q: Il y a deux conférences auxquels j'aimerais assister qui se
      donnent en même temps.
    </p>
  </div>

  <div class="answer">
    <p>
      R: Ce n'est pas une question! Toutefois, si cela arrive,
      plusieurs options s'offrent à toi. Premièrement, tu peux
      demander à un ami d'aller à l'autre conférence et de prendre des
      notes pour toi. Sinon, va voir le conférencier en question et
      demande-lui de te fournir des notes ou d'autres références qui
      te permettront d'en apprendre plus sur le sujet.
    </p>
  </div>

  <div class="question" id="requirements">
    <p>
      Q: Il y a une conférence qui me semble très intéressante, mais
      dont je n'ai pas les prérequis. Puis-je y aller malgré tout?
    </p>
  </div>

  <div class="answer">
    <p>
      R: Souvent, on peut apprécier une conférence sans comprendre
      parfaitement tous ses détails. Si tu es intéressé par le sujet,
      vas-y! Dans le pire des cas, tu auras une motivation d'en
      apprendre plus sur le sujet. N'hésites pas non plus à aller
      discuter avec le conférencier après la conférence pour parler
      des points que tu n'as pas saisis.
    </p>
  </div>

