<div class="box">
  <p>
    Bienvenue sur le site internet du Congrès Canadien des Étudiants
    en Mathématiques, le plus grand congrès en Amérique du Nord
    destiné aux étudiants en mathématiques de premier cycle
    universitaire. Le prochain congrès aura lieu à Montréal du 10
    juillet au 14 juillet. C'est l'Université de Montréal, perchée sur
    le Mont-Royal, qui aura l'honneur d'accueillir les étudiants de
    partout au Canada pour la vingtième édition de ce grand
    rassemblement.
  </p>

  <p>
    Le congrès offre une opportunité unique pour les étudiants
    d'acquérir une première expérience de recherche. Par le biais de
    rencontres et d'exposés, le CCÉM propose une occasion d'établir
    des liens entre les étudiants des différentes universités
    canadiennes, des liens dont bénéficieront les étudiants tout au
    long de leur carrière mathématique.  L'évènement offre également
    une occasion de promouvoir les différents domaines des
    mathématiques en plus d'établir des réseaux de contacts avec des
    personnes ressources oeuvrant dans les principaux domaines
    d'applications. Citons entre autres la physique, l'économie,
    l'informatique, les statistiques, les multiples génies et
    l'actuariat.
  </p>

  <p>
    Le congrès réunit plus de cent-cinquante étudiants des divers
    milieux académiques qui se présenteront mutuellement des sujets
    qui les fascinent. C'est une expérience enrichissante pour les
    jeunes mathématiciens talentueux de sortir de leur contexte de
    travail habituel pour se réunir et discuter de leurs intérêts
    communs. Il s'agit d'un évènement exempt de compétition et nous
    croyons qu'un tel contexte encourage l'échange et la diversité, ce
    qui est essentiel pour le développement des mathématiques à
    travers le pays.
  </p>

  <p>
    Ce site internet présente une mine d'information sur tout ce qui
    touche l'évènement. Si vous ne parvenez pas à trouver ce que vous
    cherchez, n'hésitez pas à écrire
    à <a href="mailto:ccem@ccem.math.ca">ccem@ccem.math.ca</a>. Il
    nous fera plaisir de répondre à vos questions.
  </p>
</div>
