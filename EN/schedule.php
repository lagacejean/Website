  <h2>Schedule</h2>

  <p>
    The CUMC's 2013 edition will take place over 5 days : from the
    10th to the 14th of July. The first day will be reserved for
    welcoming participants, a first keynote speaker and will be
    followed by a traditional opening banquet. During the congress,
    participants will have the chance to attend the talks of their
    colleagues and other keynote speakers. Snacks and coffee will be
    served during breaks between the talks and lunch will be served
    everyday around noon. To end this congress beautifully, a closing
    banquet will be organized to end this great reunion. A schedule
    will soon be available.
