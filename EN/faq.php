  <div class="question" id="which-subject">
    <p>
      Q: I want to give a talk. Which subject should I talk about?
    </p>
  </div>

  <div class="answer">
    <p>
      A: Speak about something you find interesting. If your subject
      has captivated you, chances are it will do the same to your
      audience.  Obviously, it is better if there is a link with
      mathematics, but that link can be tenuous. Do not forget that
      one of the values of the event is diversity. As such, we wanna
      hear about subjects that will be as different as they can
      be. What you talk about can come from something you liked in
      whan of your classe, something you read recently, a subject
      that's puzzling you, a research subject, a problem that prevents
      you from sleeping, or anything else that interests you in math!
    </p>

    <p>
      If you think your talk will only be accessible to students that
      are quite knowledgeable in a specific domain, you can say so in
      your abstract. This way, people who attend your talk will not
      have any bad surprises. However, it is best to assume that your
      audience does not have all of the prerequisites. After all, CUMC
      is all about discoveries.
    </p>
  </div>

  <div class="question" id="not-expert">
    <p>
      Q:There is a subject that I find interesting, but I'm no expert
      at it.  Should I still talk about it?
    </p>
  </div>

  <div class="answer">
    <p>
      A: Absolutely! Preparing your talk will be a great occasion to
      learn about the subject for you. Make sure, though, to get ready
      long enough in advance to be at ease with your subjec. This way,
      you will be able to be a little less stressed in front of your
      audience. Do not forget that if you are not an expert on your
      field, your audience probably won't be either. They will
      certainly appreciate to hear how you understood things and why
      they interested you.
    </p>
  </div>

  <div class="question" id="how-long">
    <p>
      Q: What's the length of a talk? How long should my talk be?
    </p>
  </div>

  <div class="answer">
    <p>
      A: You have got two choices : 20 or 45 minutes. If you don't
      know which length to choose, here is my advice : make it 20
      minutes. Why? Because it is an ideal length! Indeed, speaking
      for 45 minutes involves a lot of effort, even more so if it is
      your first time. Vecause a shorter talk cannot have as much
      content, it will force you to choose only the most important
      parts of the subject you want to talk about. Your audience will
      appreciate a shorter talk that is to the point. Thus, it will be
      easier to maintain contact with the public with s shorter
      talk. If you feel like you will still have much to say after 20
      minutes, remember that after your talk, those people that got
      interested with your subject will come and talk to you about it
      to learn more.
    </p>

    <p>
      Once the schedule is set, you will have an alloted time period
      that will correspond to the length you asked. To meet
      organizational needs, we would ask that you respect those time
      limits so that everyone can present their talk. Do not forget to
      practice before your talk to make sure it does fit during the
      alloted time. There's nothing we'd hate more than to stop you!
    </p>
  </div>

  <div class="question" id="time-myself">
    <p>
      Q: How can I know the length of my talk?
    </p>
  </div>

  <div class="answer">
    <p>
      A: Some speakers are proposing a strict relation between the
      number of slides and the length of a talk. There is no general
      agreement about it, but there is a lot to find ont he internet
      on the subject.  I can only have one piece of advice: practice,
      and, above all, time yourself. Don't do it only once! This way,
      you will know how much time you need, where you're losing time,
      and what you need to cut. Do not forget that it is harder to
      keep an audience's attention for 45 minutes than for 20, but if
      your talk is interesting and you are well prepared, it can pass
      in a breeze.
    </p>
  </div>

  <div class="question" id="which-language">
    <p>
      Q: Which language should my talk be in? French or English?
    </p>
  </div>

  <div class="answer">
    <p>
      A: You should present your talk in whichever language you are
      most confortable with. You can also try to speak one language
      and have your slides in the other. You also have the possibility
      to accept questions in both languages. At any rate, we advise
      you to precise these possibilities in your abstract. This way,
      you invite people whose first language is not yours to still
      come and attend your talk.
    </p>
  </div>

  <div class="question" id="get-confident">
    <p>
      Q: I'm very nervous; this is the first time I give a talk.  What
      should I do about it?
    </p>
  </div>

  <div class="answer">
    <p>
      A: Relax, take it easy! There is many a good answer to this
      question.  Above all, keep in mind that even if your talk is not
      perfect it's not the end of the world; there is no good or bad
      talks, only talks we're interested in or not. By being a tad
      witty in your presentation you will be able to ensure that your
      audience will be following along your talk, and that they will
      be happy about it.
    </p>

    <p>
      However, there are a few mistakes that are easy to avoid.  In
      general, being well prepared is the best way to be at ease on
      the stage. This will allow you to avoid mistakes due to being
      nervous. We cannot stress this enough : you cannot be prepared
      too much.  Ask someone to get a preview of your talk, and get
      their comments.  Once you will be in Montreal, make sure to
      check the classroom in which you will be presenting your
      talk. On top of it, the organizers will be happy to answer your
      questions once you are there. In fact, there will be a talk on
      the subject of <em>How to give a good talk</em> at the beginning
      of the conference. This way, you will even have some last minute
      advice.  If you have any other
      questions, <a href="mailto:communication@cumc.math.ca">contact
      us</a>!
    </p>
  </div>

  <div class="question" id="same-time">
    <p>
      Q: There is two talks I'd like to attend that are given at the
      same time.
    </p>
  </div>

  <div class="answer">
    <p>
      A: This ain't even a question! However, should this happen,
      there's many things you can do. First of all, you can ask one of
      your friends to attend the other talk and take some notes for
      you.  Else, you can go talk to the speaker and ask him to give
      you notes or other references that will allow you to learn more
      on the subject.
    </p>
  </div>

  <div class="question" id="requirements">
    <p>
      Q: There is a talk that seems very interesting to me, but I
      haven't got all the prerequisites. Can I still attend it?
    </p>
  </div>

  <div class="answer">
    <p>
      A: Often, one can manage to get the big picture of a talk
      without understanding every tiny details. If the subject has
      your interest, go for it!  vas-y! Worse comes to worse, you will
      be even more motivated to learn about it. Go ahead and discuss
      with the speaker after his talk. That way, he will be able to
      expalin some finer points you might not have understood.
    </p>
  </div>

