  <div class="question-first" id="funding-myself">
    <p>
      Q: How can I obtain funding for my expenses due to the CUMC?
    </p>
  </div>

  <div class="answer-first">
    <p>
      A: Begin with contacting your student association. They may have
      made arrangements for students that want to attend the
      conference. Also, if you are doing research over the summer, you
      could talk with your advisor. He might have funds
      available for your trip, or maybe he could even fund you using
      his research funds!
    </p>

    <p>
      Also contact your department, your faculty, or any organization
      you're associated with. Talk to them about the CUMC and show
      them our website and our poster. Tell them in which situation
      you are in. To convince them, do not forget you will represent
      your university at the CUMC. It is this way that most
      participants can come every year. In fact, most departments and
      faculties have funds for situations like this one. To make your
      argument more convincing and prove your implication, prepare a
      talk. You will have more ease proving how you will concretly
      represent your university this way.
    </p>
  </div>

  <div class="question" id="better-participant">
    <p>
      Q: Will other participants be better than me in mathematics?
    </p>
  </div>

  <div class="answer">
    <p>
      A: The CUMC is for all undergraduate students who are interested
      in mathematics and its related fields. The congress is not a
      competition, but rather a reunion of mathematics lovers from
      everywhere in Canada and even sometimes other countries! We
      strongly recommend students of every level to attend and recall
      that there will be no element of competition during the
      congress, except, obviously, the candidates for the organization
      of the CUMC 2014!
    </p>
  </div>

