  <div class="question" id="which-subject">
    <pt>
      Q: I wish to give a talk. What should I talk about?
</p>
</div>

<div class="answer">
  <p>
    A: Talk about something that interests you. If you believe the
    subject is fascinating, there are good chances that your public
    also likes it. Obviously, the subject better be related to
    mathematics, but it can be understood in a broader sense. Do not
    forget one of the values of the event is to promote
    diversity. Possible choices of subjects, without any restrictions,
    could be around things you have seen in one of your courses and
    you liked, something you recently read, something you already hit
    your head on, a research project... all ideas are relevant!
  </p>

  <p>
    If you think your talk will be accessible only to students with a
    strong background in a precise subject, it is possible to indicate
    it when you submit your abstract.
  </p>
</div>

<div class="question" id="not-expert">
  <p>
    Q: I think this subject is interesting, but I am not an expert in
    it. Should I still talk about it?
  </p>
</div>

<div class="answer">
  <p>
    A: Certainly! The preparation of your talk will be an opportunity
    for you to learn more on the subject. Make sure to prepare
    yourself long enough in advance to get a certain ease in your
    subject before you present your talk! Anyway, your public will
    probably not be an expert in the subject either. Nonetheless, to
    have no regrets, it is better to avoid high expectations. Rely on
    your own capacities.
  </p>
</div>

<div class="question" id="how-long">
  <p>
    Q: What is the length of a talk? How long should my talk last?
  </p>
</div>

<div class="answer">
  <p>
    A: There are two options : 20 minutes and 45 minutes. If you don't
    know what length to choose, here's a suggestion : take 20
    minutes. Why? Because it's an ideal format! Speaking for 45
    minutes requires a certain effort if it's your first time. Since a
    20 minute talk requires less content, it will help to maintain
    contact with the public and allows spending more time on essential
    points. Other participants will appreciate such a talk. If you
    have too much to say, just remember that after the talk, people
    that are more interested in your subject will stay to talk with
    you about it.
  </p>
</div>

<div class="question" id="time-myself">
  <p>
    Q: How do I determine the length of my talk?
  </p>
</div>

<div class="answer">
  <p>
    A: Try to evaluate the amount of material you would like to
    present. Practice and time yourself more than once! You will know
    how much time you need. Do not forget that it's harder to keep an
    audience's attention for 45 minutes than for 20
    minutes. Nevertheless, if your subject is interesting and you are
    well prepared, 45 minutes can pass by pretty fast. It is up to
    you.
  </p>
</div>

<div class="question" id="which-language">
  <p>
    Q: Should I give my talk in French or English?
  </p>
</div>

<div class="answer">
  <p>
    A: You should give your talk in the language you are most
    comfortable with. You can also try to give it in a language and
    prepare slides in the other one. Finally, it is also possible to
    mention the audience that you can take questions in French and
    English.
  </p>
</div>

<div class="question" id="get-confident">
  <p>
    Q: I am really nervous, because this is the first time I am giving
    a talk. What tips do you have for me?
  </p>
</div>

<div class="answer">
  <p>
    A: Relax and take a deep breath. There are many good answers to
    this question. Just keep in mind that even if your talk is not
    perfect, it's okay. There are no bad or good talks, only talks
    that interests us or not. By adding a touch of humour to the talk,
    you'll make sure that your audience will follow you all along and
    you'll make everything more interesting.
  </p>

  <p>
    Nonetheless, there are some mistakes that are easy to prevent. In
    general, a good preparation is the best way to ensure
    confidence. It allows you to prevent most mistakes due to
    nervosity. For instance, it is useless to go faster if you are
    lacking time. It is better to just skip some parts of the
    talk. The audience will probably not even notice!
  </p>
</div>

<div class="question" id="same-time">
  <p>
    Q: There are two talks I want to attend that are happening at the
    same time. What should I do?
  </p>
</div>

<div class="answer">
  <p>
    A: If it happens, many options are available to you. First, you
    can ask a friend to go see the other talk and take notes for
    you. If you can't, go see the speaker of the other talk and ask
    him to give you notes or references to allow you to learn more on
    the subject later.
  </p>
</div>

<div class="question" id="requirements">
  <p>
    Q: There is a talk that seems very interesting, but I don't have
    all the prerequisites. Can I go anyway?
  </p>
</div>

<div class="answer">
  <p>
    A: We can often appreciate a talk without completely understanding
    all the details perfectly. If you are interested by the subject,
    then go. In the worst scenario, you'll obtain motivation to learn
    the necessary background to understand everything and you'll have
    learned something new. Don't hesitate to talk with the speaker
    after the talk about points you didn't understand.
  </p>
</div>

