  <div class="question-first" id="funding-myself">
    <p>
      Q: How can I get funding for my CUMC expenses?
    </p>
  </div>

  <div class="answer-first">
    <p>
      A: Start by contacting your student society. It is possible that
      they have made arrangements for students who want to attend the
      confrerence. What's more, if you are doing research during the
      summer, talk about it with your supervisor. They often have
      funding for that type of event.
    </p>

    <p>
      You can also try asking your department, faculty, or any
      departement with which you are affiliated. You can bring up the
      CUMC, show them our website and our poster. You can not only
      explain your situation and how the conference will be profitable
      for you, you can also bring up the fact that you will represent
      your university in a community of mathematicians from all around
      Canada. In fact, this is how most attendees manage to come to
      CUMC each year. Indeed, most departments and faculties have a
      specific fund for conferences and scientific events. It is a
      good thing to prepare a talk, so they can really see how much
      you are involved in that conference, and how well you would
      represent your university here.
    </p>

    <p>
      The <a href="http://studc.cms.math.ca/index.html">STUDC</a> is
      also funding a $500 scholarship for the expenses of one
      student. You can find more information about it in this
      <a href="http://studc.cms.math.ca/cumc/fundingApplications/CUMC_Scholarship.pdf">document</a>
      [pdf].
    </p>
  </div>

  <div class="question" id="better-participant">
    <p>
      Q: Will the other attendees be better than me at mathematics?
    </p>
  </div>

  <div class="answer">
    <p>
      A: CUMC is for all undergraduate students that are interested in
      mathematics and other related fields. The conference is not a
      competition, it is an occasion for math lovers from all across
      Canada and beyond to get together. We strongly advide students
      from all levels to attend. Remember that there will be no
      competitions during the conference, except obviously on the
      occasion of the hosting bid for CUMC 2014. It's not about the
      other attendees having an easier time in math than you or not,
      it's about wanting to meet them!
    </p>
  </div>

  <div class="question" id="half-present">
